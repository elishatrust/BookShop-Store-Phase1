    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
        // TODO add your handling code here:
            String _uname = txt_uname.getText();
            String _passwd = txt_passwd.getText();
            
            try{         
            pst = con.prepareStatement("SELECT id,username,passcode FROM member WHERE (username=? and passcode=?)");
            pst.setString(1,_uname);
            pst.setString(2,_passwd);
            rs = pst.executeQuery();
            
            if(rs.next()==true)
            {
                BookShop bs = new BookShop();
                bs.setVisible(true);
                setVisible(false);
                this.dispose();
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Incorrect username or password","Message",JOptionPane.YES_OPTION);
                txt_uname.setText("");
                txt_passwd.setText("");
                txt_uname.requestFocus();
            }
                
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_loginBtnActionPerformed

